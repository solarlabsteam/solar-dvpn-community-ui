import {
  type CountryNodesInfoBase,
  type Node,
  type NodesData,
  type NodesSearchParameters,
  type Pagination,
  NodesOrder,
  NodeStatus,
} from "@/types";
import { formatObjectKeysToCamelCase } from "@/utils/formatters";
import { apiProvider } from "@/api";

interface INodesApi {
  getActiveNodes(params?: NodesSearchParameters): Promise<NodesData>;

  getNodes(
    continent?: string,
    country?: string,
    status?: NodeStatus,
    minPrice?: number | string,
    maxPrice?: number | string,
    orderBy?: NodesOrder,
    query?: string,
    page?: number
  ): Promise<NodesData>;

  getNodesByAddresses(addresses: string[], page?: number): Promise<NodesData>;

  getCountries(): Promise<CountryNodesInfoBase[]>;
}

class NodesApi implements INodesApi {
  private readonly apiPath = "nodes";

  getActiveNodes(params?: NodesSearchParameters): Promise<NodesData> {
    return this.getNodes(
      params?.continent,
      params?.country,
      NodeStatus.active,
      params?.minPrice,
      params?.maxPrice,
      params?.orderBy,
      params?.query,
      params?.page
    );
  }

  /**
   * @param continent [Optional] Two-symbol code of continent
   * @param country [Optional] Two-symbol ISO code of country
   * @param status [Optional] STATUS_ACTIVE or STATUS_INACTIVE, we have NodeStatus enum for it
   * @param minPrice [Optional] Minimal price (in udvpn)
   * @param maxPrice [Optional] Maximum price (in udvpn)
   * @param orderBy [Optional] PRICE, DOWNLOAD_SPEED, UPLOAD_SPEED or PEERS, we have NodesOrder enum for it
   * @param query [Optional] Some text string (e.g. Moniker)
   * @param page [Optional] Number of page, 100 nodes on page
   */
  getNodes(
    continent?: string,
    country?: string,
    status?: NodeStatus,
    minPrice?: number | string,
    maxPrice?: number | string,
    orderBy?: NodesOrder,
    query?: string,
    page?: number
  ): Promise<NodesData> {
    return apiProvider
      .get(this.apiPath, {
        params: {
          continent,
          country,
          status,
          min_price: minPrice,
          max_price: maxPrice,
          order_by: orderBy,
          q: query,
          page,
        },
      })
      .then((response) => ({
        pagination: <Pagination>formatObjectKeysToCamelCase(response.data),
        items: response.data.data.map(formatObjectKeysToCamelCase),
      }));
  }

  /**
   * @param addresses - must be unique and not empty
   * @param page - [Optional] Number of page, 100 nodes on page
   */
  getNodesByAddresses(addresses: string[], page?: number): Promise<NodesData> {
    return apiProvider
      .post(
        "nodesByAddress",
        { blockchain_addresses: addresses },
        { params: { page } }
      )
      .then((response) => ({
        pagination: <Pagination>formatObjectKeysToCamelCase(response.data),
        items: response.data.data.map(formatObjectKeysToCamelCase),
      }));
  }

  getCountries(): Promise<CountryNodesInfoBase[]> {
    return apiProvider
      .get("countries")
      .then((response) => response.data.map(formatObjectKeysToCamelCase));
  }
}

